#게시글 테이블
CREATE TABLE `boards` (
                           `b_id`	int unsigned AUTO_INCREMENT PRIMARY KEY COMMENT '게시글 PK',
                           `title`	TEXT NOT NULL COMMENT '제목',
                           `content`	TEXT NOT NULL COMMENT '내용',
                           `post_time` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '작성 시간'
);

CREATE TABLE `relations`(
                            `br_id`	int unsigned AUTO_INCREMENT PRIMARY KEY COMMENT 'PK',
                            `b_id`	int unsigned COMMENT '게시글 FK',
                            `terms`	TEXT COMMENT '형태소 리스트',
                            `tf_idf` TEXT COMMENT 'TF-IDF 분석치',
                            `is_total` tinyint NOT NULL DEFAULT 0 COMMENT 'true인 레코드는 모든 게시글의 형태소 리스트를 저장',
                            FOREIGN KEY (b_id) REFERENCES boards (b_id) ON DELETE CASCADE ON UPDATE CASCADE
);

